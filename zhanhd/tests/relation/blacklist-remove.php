 */
use System\Stdlib\Object,
    System\Swoole\ReqResHeader;

/**
 *
 */
use Zhanhd\ReqRes\Account\Signup\Response as SignupResponse,
    Zhanhd\ReqRes\Account\InitResponse,
    Zhanhd\ReqRes\Relation\Blacklist\Remove\Request,
    Zhanhd\ReqRes\Relation\Blacklist\Remove\Response;

/**
 *
 */
$config = (new Object)->import(getlongopt(array(
    'host'  => '192.168.1.186',
    'port'  => 14924,
    'login' => 1,

    'pids' => false,
)));

/**
 *
 */
$robot = new Robot($config->login);

/**
 *
 */
$robot->on(3, function($c, $qargv) {
    $res = new SignupResponse;
    $res->decode($qargv);
    print_r($res);
});

/**
 * on login success
 */
$robot->on(9, function($c, $qargv) use ($config) {
    $pids = explode(',', $config->pids);
    $req = new Request;
    $req->pids->resize(count($pids));
    foreach ($req->pids as $i => $o) {
        $o->intval($pids[$i]);
    }
    $c->send(Robot::encodeWithHeader(168, $req));
});

/* on blacklist remove response */
$robot->on(169, function($c, $qargv){
    $res = new Response;
    $res->decode($qargv);
    print_r($res);
});

/**
 *
 */
$robot->connect($config->host, $config->port);
